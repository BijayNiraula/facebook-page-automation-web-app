import { ToastContainer, toast } from 'react-toastify';
const warningToast = (text) => {
    toast.warn(text, {
        position: "top-center",
        autoClose: 5000,
        hideProgressBar: true,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "colored",
    });

}
